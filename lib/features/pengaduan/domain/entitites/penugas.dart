class Penugasan {
  final String nama;
  final String noPelanggan;
  final String noAduan;
  final int idAduan;
  final String noTelepon;
  final String alamat;
  final String status;
  final String jenisAduan;
  final String keterangan;
  final String petugas;
  final String tanggal;
  final String jenisLaporan;
  final String targetPenyelesaian;

  Penugasan({
    required this.nama,
    required this.noPelanggan,
    required this.noAduan,
    required this.idAduan,
    required this.noTelepon,
    required this.alamat,
    required this.status,
    required this.jenisAduan,
    required this.keterangan,
    required this.petugas,
    required this.tanggal,
    required this.jenisLaporan,
    required this.targetPenyelesaian,
  });
}
